// ToolDescriptor is the model-facing description of an available tool.
type ToolDescriptor struct {
	Name        string
	Description string
	Parameters  *schema.ToolParametersSchema
	Risk        ToolRisk
}

// ToolRisk labels the expected side-effect profile of a tool.
type ToolRisk string

const (
	ToolRiskUnspecified ToolRisk = ""
	ToolRiskRead        ToolRisk = "read"
	ToolRiskWrite       ToolRisk = "write"
	ToolRiskDestructive ToolRisk = "destructive"
)

// ToolScope binds a tool to an application-defined security boundary. Values
// can be sensitive, such as filesystem roots or tenant identifiers; lifecycle
// telemetry exposes only counts and hashes derived from scopes.
type ToolScope struct {
	Kind  string
	Value string
}

// ToolSafety declares local guardrails and approval context for a tool.
// Timeouts, concurrency limits, and result limits are enforced by the SDK.
// Scopes and BusinessReason are passed to approval policies, but observations
// receive only non-sensitive hashes and counts.
type ToolSafety struct {
	Risk           ToolRisk
	Timeout        time.Duration
	MaxConcurrency int
	MaxResultBytes int
	Scopes         []ToolScope
	BusinessReason string
}

// ToolSafetyProvider is an optional extension for tools that declare execution
// limits, scopes, or business approval context.
type ToolSafetyProvider interface {
	ToolSafety() ToolSafety
}

// ToolCall is a model request to execute a tool.
type ToolCall struct {
	ID        string
	Name      string
	Arguments map[string]any
}

// ToolResult is the output of a tool call.
type ToolResult struct {
	CallID   string
	Name     string
	Content  string
	Metadata map[string]any
}

// Tool is implemented by callable agent tools.
type Tool interface {
	Name() string
	Description() string
	Call(context.Context, ToolCall) (ToolResult, error)
}

// ToolParametersSchemaProvider is an optional extension for tools with JSON Schema parameters.
type ToolParametersSchemaProvider interface {
	ParametersSchema() *schema.ToolParametersSchema
}

// ToolRiskProvider is an optional extension for tools that declare side-effect risk.
type ToolRiskProvider interface {
	Risk() ToolRisk
}

// ToolFunc adapts a function into a Tool.
type ToolFunc struct {
	ToolName        string
	ToolDescription string
	Parameters      *schema.ToolParametersSchema
	ToolRisk        ToolRisk
	Safety          ToolSafety
	Fn              func(context.Context, ToolCall) (ToolResult, error)
}

func (t ToolFunc) Name() string {
	return t.ToolName
}

func (t ToolFunc) Description() string {
	return t.ToolDescription
}

func (t ToolFunc) ParametersSchema() *schema.ToolParametersSchema {
	return schema.Clone(t.Parameters)
}

func (t ToolFunc) Risk() ToolRisk {
	return t.ToolSafety().Risk
}

func (t ToolFunc) ToolSafety() ToolSafety {
	safety := CloneToolSafety(t.Safety)
	if safety.Risk == "" {
		safety.Risk = normalizeToolRisk(t.ToolRisk)
	}
	return safety
}

func (t ToolFunc) Call(ctx context.Context, call ToolCall) (ToolResult, error) {
	if t.Fn == nil {
		return ToolResult{}, errors.New("agent: tool function is nil")
	}
	return t.Fn(ctx, call)
}

// ApprovalRequest describes a tool call awaiting permission.
type ApprovalRequest struct {
	AgentID  string
	ToolName string
	Risk     ToolRisk
	// ToolSafety includes the registered tool's limits, scopes, and business
	// reason so approval policies can enforce application-specific boundaries.
	// ToolCall can still contain sensitive arguments; approval implementations
	// should avoid logging raw requests.
	ToolSafety ToolSafety
	ToolCall   ToolCall
}

// ApprovalDecision is the result of a permission check.
type ApprovalDecision struct {
	Approved bool
	Reason   string
}

// ApprovalPolicy decides whether a tool call may execute.
type ApprovalPolicy interface {
	ApproveTool(context.Context, ApprovalRequest) (ApprovalDecision, error)
}

// ApprovalFunc adapts a function into an ApprovalPolicy.
type ApprovalFunc func(context.Context, ApprovalRequest) (ApprovalDecision, error)

func (f ApprovalFunc) ApproveTool(ctx context.Context, request ApprovalRequest) (ApprovalDecision, error) {
	return f(ctx, request)
}

// AllowAllApproval approves every tool call.
type AllowAllApproval struct{}

func (AllowAllApproval) ApproveTool(context.Context, ApprovalRequest) (ApprovalDecision, error) {
	return ApprovalDecision{Approved: true, Reason: "allowed"}, nil
}

// TraceContext carries caller-provided trace correlation metadata. Values should
// be non-sensitive identifiers because the SDK surfaces them in lifecycle
// events, observations, and structured errors.
type TraceContext struct {
	TraceID    string
	SpanID     string
	TraceState string
}

type traceContextKey struct{}

// WithTraceContext attaches trace correlation metadata to ctx. The SDK only
// reads this typed value and does not inspect arbitrary context values.
func WithTraceContext(ctx context.Context, trace TraceContext) context.Context {
	trace = normalizeTraceContext(trace)
	if trace == (TraceContext{}) {
		return ctx
	}
	return context.WithValue(ctx, traceContextKey{}, trace)
}

// TraceContextFromContext returns trace correlation metadata previously attached
// with WithTraceContext.
func TraceContextFromContext(ctx context.Context) (TraceContext, bool) {
	if ctx == nil {
		return TraceContext{}, false
	}
	trace, ok := ctx.Value(traceContextKey{}).(TraceContext)
	if !ok {
		return TraceContext{}, false
	}
	trace = normalizeTraceContext(trace)
	return trace, trace != (TraceContext{})
}

func normalizeTraceContext(trace TraceContext) TraceContext {
	return TraceContext{
		TraceID:    strings.TrimSpace(trace.TraceID),
		SpanID:     strings.TrimSpace(trace.SpanID),
		TraceState: strings.TrimSpace(trace.TraceState),
	}
}

