// Role identifies who produced a message in the agent context.
type Role string

const (
	RoleSystem    Role = "system"
	RoleUser      Role = "user"
	RoleAssistant Role = "assistant"
	RoleTool      Role = "tool"
)

// Message is one item in the conversation context managed by an Agent.
type Message struct {
	Role       Role
	Content    string
	Name       string
	ToolCallID string
	ToolCalls  []ToolCall
	Metadata   map[string]any
}

// Model is the LLM adapter used by an Agent.
type Model interface {
	Generate(context.Context, ModelRequest) (ModelResponse, error)
}

// StreamModel is an optional Model extension for adapters that can emit text
// incrementally. Agents fall back to ErrStreamingUnsupported when it is absent.
type StreamModel interface {
	Model
	Stream(context.Context, ModelRequest) (<-chan StreamEvent, error)
}

var (
	// ErrStreamingUnsupported marks models that only implement non-streaming generation.
	ErrStreamingUnsupported = errors.New("agent: streaming unsupported")
	// ErrStreamingToolCallsUnsupported marks streamed tool calls, which are not executed yet.
	ErrStreamingToolCallsUnsupported = errors.New("agent: streaming tool calls unsupported")
)

// StreamEventType identifies the kind of event produced by a streaming run.
type StreamEventType string

const (
	StreamEventDelta         StreamEventType = "delta"
	StreamEventThinkingDelta StreamEventType = "thinking_delta"
	StreamEventDone          StreamEventType = "done"
	StreamEventError         StreamEventType = "error"
	StreamEventToolCallStart StreamEventType = "tool_call_start"
	StreamEventToolCallDone  StreamEventType = "tool_call_done"
)

// StreamToolCall carries safe streamed tool-call boundary metadata. It never
// includes tool arguments or result content.
type StreamToolCall struct {
	// ID is the provider tool-call ID when available.
	ID string
	// Name is the registered tool name when available.
	Name string
	// Index is the provider stream index when available.
	Index int
}

// StreamFinishMetadata carries safe final stream completion metadata.
type StreamFinishMetadata struct {
	// Reason is the provider finish or stop reason when reported.
	Reason string
}

// StreamEvent carries one item from a streaming run. Delta contains incremental
// assistant text or provider thinking text, depending on Type. ToolCall contains
// safe metadata on tool-call boundary events. Message, Usage, and Finish are
// populated on done when available; Error is populated on error.
type StreamEvent struct {
	Type     StreamEventType
	AgentID  string
	Delta    string
	ToolCall StreamToolCall
	Message  Message
	// Usage reports final token counts when a streaming model/provider reports them.
	Usage TokenUsage
	// Finish reports safe provider completion metadata on final done events.
	Finish StreamFinishMetadata
	Error  error
}

// ModelRequest is the fully assembled prompt and capability set for one model call.
type ModelRequest struct {
	AgentID      string
	SystemPrompt string
	Messages     []Message
	Tools        []ToolDescriptor
	MCPServers   []MCPServerConfig
	ActiveSkills []skills.Skill
}

// TokenUsage reports model token counts when a provider or custom model makes
// them available.
type TokenUsage struct {
	// InputTokens is the number of tokens consumed by the model request.
	InputTokens int
	// OutputTokens is the number of tokens generated by the model response.
	OutputTokens int
	// TotalTokens is the provider-reported total token count.
	TotalTokens int
}

// ProviderDiagnostics carries safe provider metadata for model failures. It
// intentionally excludes request/response bodies, full URLs, credentials, and
// raw provider error text.
type ProviderDiagnostics struct {
	Provider     string
	HTTPStatus   int
	EndpointHost string
	RequestID    string
	// RetryAfter and RateLimit fields are bounded diagnostic header hints.
	RetryAfter         string
	RateLimitLimit     string
	RateLimitRemaining string
	RateLimitReset     string
}

// IsZero reports whether diagnostics contain no usable metadata.
func (d ProviderDiagnostics) IsZero() bool {
	return normalizeProviderDiagnostics(d) == ProviderDiagnostics{}
}

// ProviderDiagnosticsError is implemented by errors that expose sanitized
// provider metadata without requiring callers to inspect raw provider text.
type ProviderDiagnosticsError interface {
	SafeProviderDiagnostics() ProviderDiagnostics
}

// ModelErrorSubcategory identifies provider/model failure details suitable for
// log grouping and alert routing. It refines ErrorCategoryModel without
// replacing the high-level category.
type ModelErrorSubcategory string

const (
	ModelErrorSubcategoryTimeout        ModelErrorSubcategory = "timeout"
	ModelErrorSubcategoryRateLimited    ModelErrorSubcategory = "rate_limited"
	ModelErrorSubcategoryAuth           ModelErrorSubcategory = "auth"
	ModelErrorSubcategoryServerError    ModelErrorSubcategory = "server_error"
	ModelErrorSubcategoryBadRequest     ModelErrorSubcategory = "bad_request"
	ModelErrorSubcategoryDecodeError    ModelErrorSubcategory = "decode_error"
	ModelErrorSubcategoryTransportError ModelErrorSubcategory = "transport_error"
	ModelErrorSubcategoryUnknown        ModelErrorSubcategory = "unknown"
)

// ModelErrorSubcategoryError is implemented by errors that expose sanitized
// model/provider failure classification without requiring raw error parsing.
type ModelErrorSubcategoryError interface {
	SafeModelErrorSubcategory() ModelErrorSubcategory
}

// ProviderError is returned by built-in provider adapters when safe diagnostic
// metadata is available. Error intentionally omits the wrapped cause text; use
// Unwrap, errors.Is, or errors.As when programmatic access to the cause is
// needed.
type ProviderError struct {
	Message               string
	Diagnostics           ProviderDiagnostics
	ModelErrorSubcategory ModelErrorSubcategory
	Cause                 error
}

// NewProviderError wraps a provider failure with safe diagnostic metadata.
func NewProviderError(message string, diagnostics ProviderDiagnostics, cause error) *ProviderError {
	diagnostics = normalizeProviderDiagnostics(diagnostics)
	return &ProviderError{
		Message:               strings.TrimSpace(message),
		Diagnostics:           diagnostics,
		ModelErrorSubcategory: modelErrorSubcategoryFromHTTPStatus(diagnostics.HTTPStatus),
		Cause:                 cause,
	}
}

// NewProviderTransportError wraps a provider transport failure and classifies
// timeouts separately from other connection/request failures.
func NewProviderTransportError(message string, diagnostics ProviderDiagnostics, cause error) *ProviderError {
	subcategory := ModelErrorSubcategoryTransportError
	if isTimeoutError(cause) {
		subcategory = ModelErrorSubcategoryTimeout
	}
	return newProviderErrorWithModelErrorSubcategory(message, diagnostics, cause, subcategory)
}

// NewProviderDecodeError wraps a provider response decode failure.
func NewProviderDecodeError(message string, diagnostics ProviderDiagnostics, cause error) *ProviderError {
	return newProviderErrorWithModelErrorSubcategory(message, diagnostics, cause, ModelErrorSubcategoryDecodeError)
}

func newProviderErrorWithModelErrorSubcategory(message string, diagnostics ProviderDiagnostics, cause error, subcategory ModelErrorSubcategory) *ProviderError {
	err := NewProviderError(message, diagnostics, cause)
	err.ModelErrorSubcategory = normalizeModelErrorSubcategory(subcategory)
	if err.ModelErrorSubcategory == "" {
		err.ModelErrorSubcategory = ModelErrorSubcategoryUnknown
	}
	return err
}

func (e *ProviderError) Error() string {
	if e == nil {
		return ""
	}
	message := strings.TrimSpace(e.Message)
	if message == "" {
		message = "provider error"
	}
	return "agent: " + message
}

func (e *ProviderError) Unwrap() error {
	if e == nil {
		return nil
	}
	return e.Cause
}

func (e *ProviderError) SafeProviderDiagnostics() ProviderDiagnostics {
	if e == nil {
		return ProviderDiagnostics{}
	}
	return normalizeProviderDiagnostics(e.Diagnostics)
}

func (e *ProviderError) SafeModelErrorSubcategory() ModelErrorSubcategory {
	if e == nil {
		return ""
	}
	if subcategory := normalizeModelErrorSubcategory(e.ModelErrorSubcategory); subcategory != "" {
		return subcategory
	}
	if subcategory := modelErrorSubcategoryFromHTTPStatus(e.Diagnostics.HTTPStatus); subcategory != "" {
		return subcategory
	}
	return ModelErrorSubcategoryUnknown
}

// ProviderDiagnosticsFromError extracts safe provider diagnostics from an error
// chain when a built-in provider or custom model error exposes them.
func ProviderDiagnosticsFromError(err error) (ProviderDiagnostics, bool) {
	if err == nil {
		return ProviderDiagnostics{}, false
	}
	var agentErr *AgentError
	if errors.As(err, &agentErr) {
		if diagnostics := normalizeProviderDiagnostics(agentErr.ProviderDiagnostics); !diagnostics.IsZero() {
			return diagnostics, true
		}
		if agentErr.Cause != nil {
			if diagnostics, ok := ProviderDiagnosticsFromError(agentErr.Cause); ok {
				return diagnostics, true
			}
		}
	}
	var providerErr ProviderDiagnosticsError
	if errors.As(err, &providerErr) {
		if diagnostics := normalizeProviderDiagnostics(providerErr.SafeProviderDiagnostics()); !diagnostics.IsZero() {
			return diagnostics, true
		}
	}
	return ProviderDiagnostics{}, false
}

// ModelErrorSubcategoryFromError extracts a sanitized model/provider failure
// subcategory from an error chain when available.
func ModelErrorSubcategoryFromError(err error) (ModelErrorSubcategory, bool) {
	if err == nil {
		return "", false
	}
	var agentErr *AgentError
	if errors.As(err, &agentErr) {
		if subcategory := normalizeModelErrorSubcategory(agentErr.ModelErrorSubcategory); subcategory != "" {
			return subcategory, true
		}
		if agentErr.Cause != nil {
			if subcategory, ok := ModelErrorSubcategoryFromError(agentErr.Cause); ok {
				return subcategory, true
			}
		}
		if agentErr.Category == ErrorCategoryModel {
			return ModelErrorSubcategoryUnknown, true
		}
	}
	var subcategoryErr ModelErrorSubcategoryError
	if errors.As(err, &subcategoryErr) {
		if subcategory := normalizeModelErrorSubcategory(subcategoryErr.SafeModelErrorSubcategory()); subcategory != "" {
			return subcategory, true
		}
	}
	return "", false
}

func normalizeProviderDiagnostics(diagnostics ProviderDiagnostics) ProviderDiagnostics {
	diagnostics.Provider = strings.TrimSpace(diagnostics.Provider)
	diagnostics.EndpointHost = normalizeProviderEndpointHost(diagnostics.EndpointHost)
	diagnostics.RequestID = strings.TrimSpace(diagnostics.RequestID)
	diagnostics.RetryAfter = strings.TrimSpace(diagnostics.RetryAfter)
	diagnostics.RateLimitLimit = strings.TrimSpace(diagnostics.RateLimitLimit)
	diagnostics.RateLimitRemaining = strings.TrimSpace(diagnostics.RateLimitRemaining)
	diagnostics.RateLimitReset = strings.TrimSpace(diagnostics.RateLimitReset)
	if diagnostics.HTTPStatus < 100 || diagnostics.HTTPStatus > 999 {
		diagnostics.HTTPStatus = 0
	}
	return diagnostics
}

func normalizeProviderEndpointHost(endpoint string) string {
	endpoint = strings.TrimSpace(endpoint)
	if endpoint == "" {
		return ""
	}
	if parsed, err := url.Parse(endpoint); err == nil && parsed.Host != "" {
		return stripProviderEndpointUserInfo(parsed.Host)
	}
	host := endpoint
	for _, separator := range []string{"/", "?", "#"} {
		if index := strings.Index(host, separator); index >= 0 {
			host = host[:index]
		}
	}
	return stripProviderEndpointUserInfo(strings.TrimSpace(host))
}

func stripProviderEndpointUserInfo(host string) string {
	if index := strings.LastIndex(host, "@"); index >= 0 {
		return host[index+1:]
	}
	return host
}

func normalizeModelErrorSubcategory(subcategory ModelErrorSubcategory) ModelErrorSubcategory {
	switch ModelErrorSubcategory(strings.TrimSpace(string(subcategory))) {
	case "":
		return ""
	case ModelErrorSubcategoryTimeout:
		return ModelErrorSubcategoryTimeout
	case ModelErrorSubcategoryRateLimited:
		return ModelErrorSubcategoryRateLimited
	case ModelErrorSubcategoryAuth:
		return ModelErrorSubcategoryAuth
	case ModelErrorSubcategoryServerError:
		return ModelErrorSubcategoryServerError
	case ModelErrorSubcategoryBadRequest:
		return ModelErrorSubcategoryBadRequest
	case ModelErrorSubcategoryDecodeError:
		return ModelErrorSubcategoryDecodeError
	case ModelErrorSubcategoryTransportError:
		return ModelErrorSubcategoryTransportError
	case ModelErrorSubcategoryUnknown:
		return ModelErrorSubcategoryUnknown
	default:
		return ModelErrorSubcategoryUnknown
	}
}

func modelErrorSubcategoryFromHTTPStatus(status int) ModelErrorSubcategory {
	switch {
	case status == 0:
		return ModelErrorSubcategoryUnknown
	case status == 401 || status == 403:
		return ModelErrorSubcategoryAuth
	case status == 429:
		return ModelErrorSubcategoryRateLimited
	case status >= 400 && status <= 499:
		return ModelErrorSubcategoryBadRequest
	case status >= 500 && status <= 599:
		return ModelErrorSubcategoryServerError
	default:
		return ModelErrorSubcategoryUnknown
	}
}

func isTimeoutError(err error) bool {
	if err == nil {
		return false
	}
	if errors.Is(err, context.DeadlineExceeded) {
		return true
	}
	var timeout interface {
		Timeout() bool
	}
	return errors.As(err, &timeout) && timeout.Timeout()
}

